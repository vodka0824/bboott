function getTeamWithFlag(teamName) {
    if (!teamName) return '🏳️ 未知';
    for (const key of Object.keys(FLAGS)) {
        if (teamName.includes(key)) return `${FLAGS[key]} ${teamName}`;
    }
    return `🏳️ ${teamName}`; // 預設白旗或無國旗
}

// 產生隨機票號
function generateTicketId() {
    return 'TK-' + Math.random().toString(36).substring(2, 10).toUpperCase();
}

